package capitulo03_operadoes;

public class Operadores {
    public static void main(String[] args) {

        int a = 3;
        int b = 9;
        System.out.println(a + b);

        System.out.println("---------------");
        a = 5;
        b = 20;
        System.out.println(a - b);

        System.out.println("----------------");


        a = 2;
        b = 1;
        System.out.println(a / b);

        System.out.println("----------------");


        int resultado;
        resultado = 2 + 2;
        System.out.println(resultado);


        System.out.println("----------------");
        int p = 20;
        p += 5;
        System.out.println(p);

        System.out.println("----------------");
        int r = 20;
        r -= 5;
        System.out.println(r);

    }
}
